define(function(require) {
	return Backbone.Model.extend({
		url:function(){
			if(this.isNew()){
				return '/api/teamplayer/';
			} else {
				return '/api/teamplayer/?id=' + this.id;
			}
		},
		parse : function(resp) {
			//if(resp.results && resp.results)
			if(resp.rv){
				return resp.rv.teamplayer;
			}
			return resp;
		}
	});
});